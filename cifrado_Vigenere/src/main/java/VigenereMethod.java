    private void btnEncriptarMVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEncriptarMVActionPerformed
        Vigenere objVigenere = new Vigenere();
        if(!txtEntradaMV.getText().isEmpty() && !txtClave1.getText().isEmpty())
        {
            objVigenere.setEntradaNC(txtEntradaMV.getText());
            objVigenere.setClaveNC(txtClave1.getText());
            txtEncriptadoMV.setText(objVigenere.Encriptacion());
        }
        else{
            JOptionPane.showMessageDialog(this, "Introduzca el texto a encriptar y la clave", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEncriptarMVActionPerformed

    private void btnDesencriptarMVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDesencriptarMVActionPerformed
        Vigenere objVigenere = new Vigenere ();
        if(!txtEncriptado2MV.getText().isEmpty() && !txtClave2.getText().isEmpty())
        {
            objVigenere.setEntradaC(txtEncriptado2MV.getText());
            objVigenere.setClaveC(txtClave2.getText());
            txtDesencriptadoMV.setText(objVigenere.Desencriptacion());
        }
        else{
            JOptionPane.showMessageDialog(this, "Introduzca el texto a desencriptar y la clave", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDesencriptarMVActionPerformed

